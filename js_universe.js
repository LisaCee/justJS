// primitive values
console.log(2);
console.log("hello");
console.log(undefined);

// objects and functions
console.log({});
console.log([]);
console.log(x => x * 2);

// expressions = questions that JS can answer
console.log(2 + 2);

// types
console.log(typeof(2));
console.log(typeof("hello"));
console.log(typeof(undefined));

console.log(typeof({}));
console.log(typeof([]));
console.log(typeof(x => x * 2));

// without parens
console.log(typeof {});
console.log(typeof []);
